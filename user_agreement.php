<p>Mir ist bekannt, dass ich mich jederzeit an den <a href="https://tu-dresden.de/tu-dresden/organisation/gremien-und-beauftragte/beauftragte/datenschutzbeauftragter">Datenschutzbeauftragten der TU Dresden</a> sowie an die <a href="https://www.saechsdsb.de/">zuständige Aufsichtsbehörde</a> für den Datenschutz wenden kann.</p>

