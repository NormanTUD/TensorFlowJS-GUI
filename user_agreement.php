<h1>Datenschutzerklärung / Einwilligungserklärung für das Forschungsprojekt &raquo;TensorFlow Demonstrator&laquo;</h1>
<h2>1. Zweck und datenerhebende Stelle</h2>

<p>Im Rahmen des Forschungsprojekts &raquo;ScaDS&laquo; soll untersucht werden, wie automatische Bildsegmentierung per Computeralgorithmus anhand von wenigen Beispieldaten funktioniert.</p>

<p>Alleinig für vorgenannten Forschungszweck sollen personenbezogene Daten durch die TU Dresden (für das Projekt <a href="https://scads.de">&raquo;ScaDS&laquo;</a>, Chemnitzer Straße 46b 01187 Dresden, E-Mail: <a href="mailto:service@scads.de">service@scads.de</a>) verarbeitet werden (Verarbeitung im Sinne von Erhebung, Speicherung, Veränderung und Nutzung). Hierzu ist eine freiwillige und informierte Einwilligung erforderlich.</p>

<h2>2. Datenverarbeitung</h2>

<p>Die Einwilligungserklärung umfasst die Verarbeitung folgender personenbezogener Daten:</p>

<ul>
	<li>angegebene Email-Adresse,</li>
	<li>angegebener Nutzername (muss nicht mit Ihrem Klarnamen übereinstimmen!),</li>
	<li>angegebenes Passwort,</li>
	<li>die hochgeladenen Bilder und Segmentierungshilfen.</li>
</ul>

<h2>3. Anonymität </h2>

<p>Die Datenerhebung erfolgt personenbezogen. Das bedeutet, dass auf Grund der erhobenen Daten Rückschlüsse auf Sie gezogen werden können. Wir sichern jedoch zu, dass die Daten unverzüglich anonymisiert werden, sobald es der Forschungszweck erlaubt. Es kann jedoch unter Umständen und im Einzelfall möglich sein, dass keine vollständige Anonymisierung möglich ist.</p>

<h2>4. Datenübermittlung</h2>

<p>Insofern dies nicht anders gesetzlich bestimmt ist oder Sie im Einzelfall ausdrücklich eingewilligt haben, erfolgt keine Übermittlung von personenbezogenen Daten an Dritter</p>

<h2>5. Veröffentlichung</h2>

<p>Die Forschungsergebnisse werden in wissenschaftlich üblicher Form veröffentlicht. Wir sichern zu, dass aus den Veröffentlichungen keinerlei Rückschlüsse auf natürliche Personen möglich sind.</p>

<h2>6. Freiwilligkeit und Widerruf</h2>

<p>Die Teilnahme am Forschungsprojekt ist freiwillig. Eine Nichtteilnahme hat keine Folgen!</p>

<p>Diese Einwilligung kann jederzeit schriftlich und formlos bei der datenerhebenden Stelle und mit Wirkung auf die Zukunft widerrufen werden. In diesem Fall reicht eine formlose Email an <a href="mailto:service@scads.de">service@scads.de</a> bzw. über das <a href="contact.php">Kontaktformular</a>.</p>

<h2>7. Speicherdauer</h2>

<p>Alle personenbezogenen Daten werden nach Abschluss des Forschungsprojektes datenschutzgerecht gelöscht.

